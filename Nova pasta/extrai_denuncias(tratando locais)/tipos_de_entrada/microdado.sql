        d.den_cd as denuncia,
        d.den_logr_tp, 
        d.den_logr_ds, 
        d.den_logr_num, 
        d.den_logr_bairro, 
        d.den_logr_subbairro, 
        d.den_logr_cep, 
        d.den_logr_mun , 
        d.den_loc_ref,
        d.den_dt_rec,
        at.tpa_cd as assunto_cd,
        at.tpa_ds as assunto,
        at.tpa_cla_cd as fk_assunto_classe,
        at.tpa_st as assunto_status,
        cla.cla_cd,
        cla.cla_ds,
        cla.cla_st,
        x2.xpt_cd as xpto_cd,
        x2.xpt_ds as xpto,
        x2.xpt_st as xpto_status,
        m.mul_cd as mulher_cd,
        m.mul_den_cd,
        m.mul_lugar,
        m.mul_vive,
        m.mul_filhos,
        m.mul_filhos_qtd,
        m.mul_filhos_agd,
        m.mul_agr_alcool,
        m.mul_quando,
        m.mul_freq,
        m.mul_hora,
        m.mul_test,
        m.mul_t_pai_vit,
        m.mul_t_mae_vit,
        m.mul_t_pai_agr,
        m.mul_t_mae_agr,
        m.mul_t_filhos,
        m.mul_t_outros,
        m.mul_t_ods,
        m.mul_data,
        m.mul_tpag_fisica,
        m.mul_tpag_verbal,
        m.mul_tpag_sexual,
        m.mul_tpag_carcere,
        m.mul_tpag_ameaca,
        m.mul_tpag_ninform,
