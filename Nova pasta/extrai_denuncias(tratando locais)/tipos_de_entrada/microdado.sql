        v.com_telefone,
        v.com_doc,
        v.com_tipo,
        v.ro_dp,
        v.seguradora,
        v.detalhes
        
        
from 
		((dd.dbo.assunto_denuncia a full  JOIN dd.dbo.assunto_tipo at ON (a.ass_tpa_cd = at.tpa_cd) full JOIN dd.dbo.assunto_classe cla ON (cla.cla_cd=at.tpa_cla_cd)) full  JOIN dd.dbo.denuncia d ON (d.den_cd = a.ass_den_cd))    
		full outer join 
		((dd.dbo.xpto x2  join dd.dbo.xpto_denuncia x3 on (x2.xpt_cd=x3.dxp_xpt_cd)) join dd.dbo.denuncia d2 on (x3.dxp_den_cd=d2.den_cd))
		 on (d2.den_cd = d.den_cd)
          full join (dd.dbo.dd_mulher m  join dd.dbo.denuncia d3 on (m.mul_den_cd=d3.den_cd)) on (d3.den_cd=d.den_cd)
		 full join (dd.dbo.veiculos v join dd.dbo.vei_modelo vm on(v.marca=vm.mod_cd) join dd.dbo.vei_marca vei_marca ON (vei_marca.mar_cd=vm.mar_cd) join dd.dbo.denuncia d4 on (v.den_cd=d4.den_cd)) on (d4.den_cd=d.den_cd)
   where d.den_cd is not NULL
	order by d.den_cd