        v.vei_cd as veiculos_cd,
        vei_marca.mar_ds,
        vm.mod_ds,
        v.cor,
        v.ano_mod,
        v.ano_fab,
        v.placa,
        v.municipio,
        v.uf,
        v.chassis,
        v.proprietario,
        v.com_nome,
        v.com_endereco,
        v.com_bairro,
        v.com_municipio,
